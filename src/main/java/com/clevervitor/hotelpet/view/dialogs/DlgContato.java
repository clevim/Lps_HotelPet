    private void BntSendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BntSendActionPerformed

        String msg = edtFromEmail.getText() + "<br>" + edtEmail.getText();

        try {
            gSender.sendEmailContato(edtFromEmail.getText(), "Contato Cliente", emBds.emailCat(msg));
            ShowMessageDialog DialMsg = new ShowMessageDialog("Sucesso", "Eniado Com sucesso");
            DialMsg.setVisible(true);
            edtEmail.disable();
            edtFromEmail.disable();
        } catch (Exception e) {
            ShowMessageDialog DialMsg = new ShowMessageDialog("Erro", "!!!Falha ao enviar!!!");
            DialMsg.setVisible(true);
        }

    }//GEN-LAST:event_BntSendActionPerformed


